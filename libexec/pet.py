# Per-frame timing (seconds) — blink is short, expression is mid
IDLE_TIMING = [0.85, 0.12, 0.70, 0.45]

def is_tty() -> bool:
    return sys.stdout.isatty()

class RawTerminal:
    """Context manager: stdin을 cbreak 모드로 전환, 커서 숨김/복원."""
    def __enter__(self):
        import tty, termios
        self.fd  = sys.stdin.fileno()
        self.old = termios.tcgetattr(self.fd)
        tty.setcbreak(self.fd)
        sys.stdout.write("\033[?25l")  # hide cursor
        sys.stdout.flush()
        return self

    def __exit__(self, *_):
        import termios
        termios.tcsetattr(self.fd, termios.TCSADRAIN, self.old)
        sys.stdout.write("\033[?25h")  # show cursor
        sys.stdout.flush()

    def read_key(self, timeout: float = 0.0) -> str | None:
        r, _, _ = _select.select([sys.stdin], [], [], timeout)
        return sys.stdin.read(1) if r else None

def apply_transform(art: list, t: dict) -> list:
    result = list(art)
    for i, line in enumerate(result):
        for old, new in t.items():
            result[i] = result[i].replace(old, new)
    return result

def _write_frame(lines: list, color: str, n_up: int):
    """Move cursor up n_up lines and overwrite with new frame."""
    sys.stdout.write(f"\033[{n_up}A")
    for line in lines:
        sys.stdout.write(f"\033[2K  {color}{line}{R}\n")
    sys.stdout.flush()

def animate_idle(pet: dict, duration: float = 4.0):
    """Idle animation: blinking + subtle expression twitches."""
    sp     = SPECIES[pet["species"]]
    stage  = get_stage(pet["species"], pet["xp"])
    art    = sp["stages"][stage]["art"]
    color  = sp["color"]
    tforms = ANIM_IDLE.get(pet["species"], [{}, {}, {}, {}])
    frames = [apply_transform(art, t) for t in tforms]
    n      = len(art)

    for line in frames[0]:
        sys.stdout.write(f"  {color}{line}{R}\n")
    sys.stdout.flush()

    elapsed, fi = 0.0, 0
    try:
        while elapsed < duration:
            fi = (fi + 1) % len(frames)
            t  = IDLE_TIMING[fi % len(IDLE_TIMING)]
            time.sleep(t)
            elapsed += t
            _write_frame(frames[fi], color, n_up=n)
    except KeyboardInterrupt:
        pass
    # Leave on base frame
    _write_frame(frames[0], color, n_up=n)
    print()

