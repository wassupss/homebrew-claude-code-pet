COMMANDS = {
    "status":   cmd_status,
    "watch":    cmd_watch,
    "roll":     cmd_roll,
    "roll10":   cmd_roll10,
    "list":     cmd_list,
    "select":   cmd_select,
    "play":     cmd_play,
    "rename":   cmd_rename,
    "codex":    cmd_codex,
    "_hook":    cmd_hook,
}

def main():
    args = sys.argv[1:]
    cmd  = args[0] if args else ""
    rest = args[1:]

    s = load_state()

    # Passive mood decay (based on days since last session)
    last = s.get("last_session_date")
    if last:
        days_ago = (date.today() - date.fromisoformat(last)).days
        if days_ago > 0:
            pet = active_pet(s)
            if pet:
                decay = days_ago * 8
                pet["mood"] = max(0, pet["mood"] - decay)
                save_state(s)

    # No-args or explicit "watch" → interactive watch mode (TTY only)
    if cmd in ("", "watch") and is_tty():
        cmd_watch(s, rest)
        return

    # No-args but piped/non-TTY → static status
    if cmd == "":
        cmd_status(s, rest)
        return

    fn = COMMANDS.get(cmd)
    if fn:
        fn(s, rest)
    else:
        print(f"  {BRED}알 수 없는 명령어: {cmd}{R}")
        print(f"  사용 가능: {', '.join(k for k in COMMANDS if not k.startswith('_'))}")

if __name__ == "__main__":
    main()
