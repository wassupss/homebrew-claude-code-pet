    if next_xp:
        stages   = SPECIES[pet["species"]]["stages"]
        cur      = stages[stage]
        prog     = pet["xp"] - cur["min_xp"]
        needed   = next_xp - cur["min_xp"]
        print(f"  XP  {BBLUE}{bar(prog, needed)}{R}  {int(pet['xp'])}/{int(next_xp)}")
    else:
        print(f"  XP  {BYELLOW}{'█'*20}{R} MAX  {BYELLOW}★{R}")
