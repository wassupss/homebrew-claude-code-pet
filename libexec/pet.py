def _pick_rarity(pity: int) -> str:
    """Weighted rarity pick with pity (guaranteed rare+ at 10 rolls)"""
    # Hidden is a separate independent roll — not affected by pity
    if random.random() < RARITY_RATE["hidden"]:
        return "hidden"
    if pity >= 9:
        # guaranteed rare or legendary
        roll = random.random()
        return "legendary" if roll < (RARITY_RATE["legendary"] / (RARITY_RATE["legendary"] + RARITY_RATE["rare"])) else "rare"
    roll = random.random()
    cumulative = 0.0
    for rarity, rate in RARITY_RATE.items():
        if rarity == "hidden":
            continue
        cumulative += rate
        if roll < cumulative:
            return rarity
    return "common"

def _pick_species(rarity: str) -> str:
    pool = [k for k, v in SPECIES.items() if v["rarity"] == rarity]
    return random.choice(pool)

def do_roll(s: dict) -> dict | None:
    if s["coins"] < ROLL_COST:
        print(f"\n  {BRED}코인이 부족해요! (보유: {s['coins']}, 필요: {ROLL_COST}){R}")
        print(f"  {DIM}Claude Code 세션을 시작하면 코인을 얻을 수 있어요.{R}")
        return None

    s["coins"]       -= ROLL_COST
    s["total_rolls"] += 1
    rarity = _pick_rarity(s["pity"])
    if rarity in ("rare", "legendary"):
        s["pity"] = 0
    else:
        s["pity"] += 1

    species_key = _pick_species(rarity)
    sp          = SPECIES[species_key]

    pet = {
        "id":       str(uuid.uuid4())[:8],
        "species":  species_key,
        "rarity":   rarity,
        "name":     sp["name"],
        "xp":       0.0,
        "mood":     80.0,
        "born_at":     date.today().isoformat(),
        "last_fed":    None,
        "last_played": None,
    }
    s["pets"].append(pet)
    if not s["active_pet_id"]:
        s["active_pet_id"] = pet["id"]
    return pet

