def cmd_hook(s: dict, args: list):
    """Internal: called by Claude Code hooks"""
    event = args[0] if args else ""
    today = date.today().isoformat()

    if event == "session_start":
        # Streak logic
        last = s.get("last_session_date")
        if last == today:
            pass  # same day, no streak change
        elif last and (date.today() - date.fromisoformat(last)).days == 1:
            s["streak"] = s.get("streak", 0) + 1
        else:
            s["streak"] = 1

        s["last_session_date"] = today
        s["total_sessions"]    = s.get("total_sessions", 0) + 1
        s["coins"]             = s.get("coins", 0) + 10  # earn coins per session

        # Give XP to active pet
        pet = active_pet(s)
        if pet:
            streak_bonus = 1.0 + min(s["streak"] * 0.05, 0.5)
            gain = SESSION_XP * streak_bonus
            old_stage = get_stage(pet["species"], pet["xp"])
            pet["xp"] += gain
            new_stage = get_stage(pet["species"], pet["xp"])

            # Mood slowly decays and regens on session
            pet["mood"] = min(100, pet["mood"] + 5)

            if new_stage > old_stage:
                sp    = SPECIES[pet["species"]]
                sdata = sp["stages"][new_stage]
                evolve_lines = SPECIES_DIALOGUE.get(pet["species"], {}).get("evolve", ["진화했어!"])
                evolve_text  = random.choice(evolve_lines)
                if is_tty():
                    animate_evolution(pet, old_stage)
                    print_speech_bubble(evolve_text, pet)
                # Print evolution notice (will appear in Claude Code output)
                print(f"\n  {sp['color']}{B}✦ {pet['name']}이(가) 진화했어요! → {sdata['name']} ✦{R}")
                print(f"  {DIM}\"{evolve_text}\"{R}\n")

        save_state(s)

    elif event == "tool_use":
        pet = active_pet(s)
        if pet:
            pet["xp"]    = pet.get("xp", 0) + TOOL_XP
            s["total_tool_calls"] = s.get("total_tool_calls", 0) + 1
            # Mood decay over time
            pet["mood"] = max(0, pet["mood"] - 0.2)
        save_state(s)

